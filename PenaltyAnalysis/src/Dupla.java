
public class Dupla {
	private Integer valor;
	private Integer frec;
	private Integer opinionAcum;
	public Dupla(Integer valor, Integer frec, Integer opinionAcum) {
		super();
		this.valor = valor;
		this.frec = frec;
		this.opinionAcum = opinionAcum;
	}
	public Integer getValor() {
		return valor;
	}
	public void setValor(Integer valor) {
		this.valor = valor;
	}
	public Integer getFrec() {
		return frec;
	}
	public void setFrec(Integer frec) {
		this.frec = frec;
	}
	public Integer getOpinionAcum() {
		return opinionAcum;
	}
	public void setOpinionAcum(Integer opinionAcum) {
		this.opinionAcum = opinionAcum;
	}
	
}
